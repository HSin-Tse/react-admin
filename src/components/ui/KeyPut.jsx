import {useState} from 'react';
import React from 'react';

export default function KeyPut  (props)  {
    // You can use Hooks here!


    return <div>
        <p>You clicked {this.state.count} times</p>
        <button onClick={() => this.setState({count: this.state.count + 1})}>
            Click me
        </button>
    </div>;
}

